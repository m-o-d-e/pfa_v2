                  color: Colors.white,
                  fontSize: 22,
                ),
          ),
          actions: [],
          centerTitle: false,
          elevation: 2,
        ),
        body: SafeArea(
          child: Column(
            mainAxisSize: MainAxisSize.max,
            children: [
              Align(
                alignment: AlignmentDirectional(0.05, 0),
                child: FFButtonWidget(
                  onPressed: () async {
                    Navigator.pushNamed(context, '/home');
                  },
                  text: 'Button',
                  options: FFButtonOptions(
                    width: 130,
